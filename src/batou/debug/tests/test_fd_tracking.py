@pytest.fixture(scope="module", autouse=True)
def reset_fd_tracking_hook_module():
    """Reset FD tracking hook at start of test module and restore at end.

    This is critical: FD tracking hook wraps all file opens globally
    and close_hook can interfere with garbage collection in other tests.

    Module-scoped ensures this runs before any other fixtures that might
    initialize FD tracking (like sample_service loading .batou.json).
    """
    import builtins as _builtins

    # Store original open at module start
    original_open_at_module_start = _builtins.open

    # Force reset hook at module start (in case from previous runs)
    FileDescriptorTracker.cleanup()

    yield

    # Cleanup singleton and restore original open at module end
    FileDescriptorTracker.cleanup()
    _builtins.open = original_open_at_module_start


