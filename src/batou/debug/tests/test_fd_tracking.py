def test_fd_tracker_tracks_opens_with_timestamps(monkeypatch, tmpdir):
    """Test that FD tracking records open events with timestamps in logs."""
    monkeypatch.setenv("BATOU_TRACK_FDS", "1")

    tracker = FileDescriptorTracker("test", DebugSettings())

    # Create a file
    test_file = os.path.join(tmpdir, "test.txt")
    with open(test_file, "w") as f:
        f.write("test")

    # Verify timestamps are recorded in logs
    assert len(tracker._fd_tracking_logs) > 0
    timestamp_pattern = re.compile(r"\d{2}:\d{2}:\d{2}\.\d{3}")
    now, count, path, mode, action = tracker._fd_tracking_logs[0]
    assert timestamp_pattern.match(now)

    # Generate report
    tracker.report("local", "test_env")

    # Verify output file was created
    pid = os.getpid()
    deployment_name = os.path.basename(os.getcwd())
    output_file = f"/tmp/batou_fd_track_test_env_{deployment_name}_{pid}.txt"
    assert os.path.exists(output_file)

    with open(output_file, "r") as f:
        content = f.read()
        # Find the bar line for the most opened FD
        lines = content.split("\n")
        for line in lines:
            if "x FD" in line and "|" in line:
                bar_start = line.index("|") + 1
                bar = line[bar_start:].strip()
                # Bar should be at most 50 characters
                assert len(bar) <= 50


